package ru.skypro.homework.dto;

import lombok.Data;

import java.util.List;

/**
 * Data Transfer Object (DTO) для представления списка объявлений с пагинацией в системе.
 *
 * <p>Используется при выводе коллекций объявлений (например, в каталоге или поиске),
 * когда необходимо передать не только сами данные, но и метаинформацию о количестве
 * элементов. Позволяет реализовать пагинацию на клиенте.
 *
 * <p>Структура соответствует типичному формату REST‑ответов со списками:
 * <ul>
 *   <li>{@code count} — общее количество объявлений по запросу;</li>
 *   <li>{@code results} — список объявлений для текущей страницы.</li>
 * </ul>
 *
 * <p><b>Примечание:</b> класс аннотирован {@link Data} от Lombok, что автоматически генерирует:
 * <ul>
 *   <li>геттеры и сеттеры для всех полей;</li>
 *   <li>реализацию {@code equals()} и {@code hashCode()};</li>
 *   <li>реализацию {@code toString()};</li>
 *   <li>конструктор по умолчанию.</li>
 * </ul>
 *
 * @see Data
 * @see Ad
 */
@Data
public class Ads {

    /**
     * Общее количество объявлений, соответствующих запросу.
     *
     * <p>Используется для:
     * <ul>
     *   <li>отображения общего числа результатов на UI;</li>
     *   <li>расчёта количества страниц при пагинации;</li>
     *   <li>информирования пользователя о масштабе выборки.</li>
     * </ul>
     *
     * <p>Например, если в системе 150 объявлений, а на страницу выводится 10,
     * то {@code count = 150}, а {@code results} будет содержать 10 элементов.
     */
    private int count;

    /**
     * Список объявлений для текущей страницы пагинации.
     *
     * <p>Каждый элемент — экземпляр {@link Ad}, содержащий основные данные
     * об объявлении (автор, изображение, идентификатор, цена, заголовок).
     *
     * <p>Используется для отображения карточек объявлений в интерфейсе.
     * Размер списка определяется параметром пагинации (например, 10–50 элементов
     * на страницу).
     *
     * @see Ad
     */
    private List<Ad> results;
}
