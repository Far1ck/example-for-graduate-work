package ru.skypro.homework.dto;

/**
 * Перечисление (enum), определяющее роли пользователей в системе.
 *
 * <p>Каждая роль задаёт набор прав доступа и разрешённых действий для пользователя
 * в рамках приложения. Используется для реализации механизма авторизации
 * и контроля доступа к функциональным возможностям системы.
 *
 * <p>Доступные роли:
 * <ul>
 *   <li>{@code USER} — стандартная роль обычного пользователя с базовым набором прав;</li>
 *   <li>{@code ADMIN} — роль администратора с расширенными правами доступа и возможностями управления системой.</li>
 * </ul>
 *
 * <p><b>Примечание:</b>
 * <ul>
 *   <li>роли используются в связке с механизмами Spring Security для настройки прав доступа;</li>
 *   <li>набор ролей может быть расширен в будущем при развитии функционала системы;</li>
 *   <li>назначение роли пользователю обычно происходит при регистрации или через административный интерфейс.</li>
 * </ul>
 *
 * @see ru.skypro.homework.dto.Register
 */
public enum Role {

    /**
     * Роль обычного пользователя системы.
     *
     * <p>Предоставляет доступ к базовому функционалу приложения, включая:
     * <ul>
     *   <li>просмотр общедоступного контента;</li>
     *   <li>работу со своим личным профилем;</li>
     *   <li>использование основных сервисов приложения в рамках пользовательского сценария.</li>
     * </ul>
     *
     * <p>Не даёт прав на:
     * <ul>
     *   <li>управление настройками системы;</li>
     *   <li>редактирование чужих данных;</li>
     *   <li>доступ к административным функциям.</li>
     * </ul>
     */
    USER,

    /**
     * Роль администратора системы.
     *
     * <p>Предоставляет полный доступ ко всем функциям приложения, включая:
     * <ul>
     *   <li>управление пользователями и их правами;</li>
     *   <li>настройку системных параметров;</li>
     *   <li>просмотр и анализ логов системы;</li>
     *   <li>выполнение операций обслуживания и администрирования.</li>
     * </ul>
     *
     * <p><b>Важно:</b> доступ к роли ADMIN должен быть строго ограничен
     * и предоставлен только доверенным пользователям с соответствующими полномочиями.
     */
    ADMIN
}
