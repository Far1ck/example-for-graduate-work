package ru.skypro.homework.dto;

import lombok.Data;

/**
 * Data Transfer Object (DTO) для представления объявления в системе.
 *
 * <p>Используется для передачи данных об объявлении между слоями приложения
 * (например, между сервисом и контроллером) при операциях получения, создания
 * или обновления объявлений. Содержит основные атрибуты объявления без
 * служебной информации.
 *
 * <p>Поля DTO соответствуют полям сущности {@link ru.skypro.homework.entity.AdEntity},
 * но представлены в упрощённом виде для внешнего использования.
 *
 * <p><b>Примечание:</b> класс аннотирован {@link Data} от Lombok, что автоматически генерирует:
 * <ul>
 *   <li>геттеры и сеттеры для всех полей;</li>
 *   <li>реализацию {@code equals()} и {@code hashCode()};</li>
 *   <li>реализацию {@code toString()};</li>
 *   <li>конструктор по умолчанию.</li>
 * </ul>
 *
 * @see Data
 * @see ru.skypro.homework.entity.AdEntity
 */
@Data
public class Ad {

    /**
     * Идентификатор автора объявления (пользователя, разместившего объявление).
     *
     * <p>Соответствует полю {@code author} в сущности {@link ru.skypro.homework.entity.AdEntity}.
     * Используется для связи объявления с пользователем, который его создал.
     */
    private int author;

    /**
     * Путь к изображению объявления (относительный или абсолютный URL).
     *
     * <p>Может быть пустым, если объявление не содержит изображения.
     * Отображается в интерфейсе рядом с заголовком и ценой объявления.
     */
    private String image;

    /**
     * Первичный ключ (идентификатор) объявления в системе.
     *
     * <p>Соответствует полю {@code id} в сущности {@link ru.skypro.homework.entity.AdEntity}.
     * Используется для однозначной идентификации объявления при операциях чтения,
     * обновления или удаления.
     */
    private int pk;

    /**
     * Стоимость товара/услуги, указанного в объявлении.
     *
     * <p>Указывается в условных единицах (например, рублях). Отображается в интерфейсе
     * как основное ценовое предложение объявления.
     */
    private int price;

    /**
     * Заголовок объявления, кратко описывающий товар/услугу.
     *
     * <p>Отображается в списках объявлений и в карточке объявления. Должен быть
     * информативным и лаконичным, чтобы привлечь внимание пользователя.
     */
    private String title;
}
