package ru.skypro.homework.dto;

import lombok.Data;

/**
 * Data Transfer Object (DTO) для расширенного представления объявления в системе.
 *
 * <p>Используется для передачи детализированной информации об объявлении, включая
 * данные об авторе (имя, фамилия, контакты), описание товара/услуги и основные
 * атрибуты объявления. Применяется в сценариях, где требуется полная информация
 * (например, при просмотре детальной карточки объявления).
 *
 * <p>Поля DTO соответствуют объединённой информации из сущностей
 * {@link ru.skypro.homework.entity.AdEntity} и {@link ru.skypro.homework.entity.UserEntity},
 * но представлены в упрощённом виде для внешнего использования.
 *
 * <p><b>Примечание:</b> класс аннотирован {@link Data} от Lombok, что автоматически генерирует:
 * <ul>
 *   <li>геттеры и сеттеры для всех полей;</li>
 *   <li>реализацию {@code equals()} и {@code hashCode()};</li>
 *   <li>реализацию {@code toString()};</li>
 *   <li>конструктор по умолчанию.</li>
 * </ul>
 *
 * @see Data
 * @see ru.skypro.homework.entity.AdEntity
 * @see ru.skypro.homework.entity.UserEntity
 */
@Data
public class ExtendedAd {

    /**
     * Первичный ключ (идентификатор) объявления в системе.
     *
     * <p>Соответствует полю {@code id} в сущности {@link ru.skypro.homework.entity.AdEntity}.
     * Используется для однозначной идентификации объявления при операциях чтения,
     * обновления или удаления.
     */
    private int pk;

    /**
     * Имя автора объявления (пользователя, разместившего объявление).
     *
     * <p>Извлекается из поля {@code firstName} сущности {@link ru.skypro.homework.entity.UserEntity}.
     * Отображается в детальной карточке объявления для идентификации продавца/исполнителя.
     */
    private String authorFirstName;

    /**
     * Фамилия автора объявления.
     *
     * <p>Извлекается из поля {@code lastName} сущности {@link ru.skypro.homework.entity.UserEntity}.
     * Дополняет информацию об авторе объявления.
     */
    private String authorLastName;

    /**
     * Подробное описание товара или услуги, предлагаемой в объявлении.
     *
     * <p>Может содержать несколько предложений, детализирующих характеристики,
     * условия сделки, особенности товара и т. п. Отображается в полной
     * версии карточки объявления.
     */
    private String description;

    /**
     * Электронный адрес автора объявления для связи.
     *
     * <p>Извлекается из поля {@code email} сущности {@link ru.skypro.homework.entity.UserEntity}.
     * Предоставляется пользователю как один из способов связи с автором объявления.
     */
    private String email;

    /**
     * Путь к изображению объявления (относительный или абсолютный URL).
     *
     * <p>Может быть пустым, если объявление не содержит изображения.
     * Отображается в карточке объявления как визуальная иллюстрация товара/услуги.
     */
    private String image;

    /**
     * Номер телефона автора объявления для связи.
     *
     * <p>Извлекается из поля {@code phone} сущности {@link ru.skypro.homework.entity.UserEntity}.
     * Предоставляется пользователю как один из способов оперативной связи
     * с автором объявления.
     */
    private String phone;

    /**
     * Стоимость товара/услуги, указанного в объявлении.
     *
     * <p>Указывается в условных единицах (например, рублях). Отображается в карточке
     * объявления как основное ценовое предложение.
     */
    private int price;

    /**
     * Заголовок объявления, кратко описывающий товар/услугу.
     *
     * <p>Отображается в списках объявлений и в карточке объявления. Должен быть
     * информативным и лаконичным, чтобы привлечь внимание пользователя.
     */
    private String title;
}
