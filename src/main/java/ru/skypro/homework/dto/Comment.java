package ru.skypro.homework.dto;

import lombok.Data;

/**
 * DTO (Data Transfer Object) для передачи данных комментария в системе.
 *
 * <p>Представляет собой простую структуру данных, содержащую информацию о комментарии,
 * включая данные об авторе, времени создания и тексте сообщения. Используется для:
 * <ul>
 *   <li>передачи данных между слоями приложения;</li>
 *   <li>сериализации в JSON при обмене с API;</li>
 *   <li>отображения информации о комментариях в пользовательском интерфейсе.</li>
 * </ul>
 *
 * <p>Класс аннотирован {@link Data}, что автоматически генерирует:
 * <ul>
 *   <li>геттеры для всех полей;</li>
 *   <li>сеттеры для всех полей;</li>
 *   <li>методы {@code equals()} и {@code hashCode()};</li>
 *   <li>метод {@code toString()};</li>
 *   <li>конструктор без аргументов.</li>
 * </ul>
 *
 * @see Data
 */
@Data
public class Comment {

    /**
     * Идентификатор автора комментария (обычно соответствует ID пользователя в системе).
     */
    private int author;

    /**
     * URL или путь к изображению профиля автора комментария.
     *
     * <p>Может быть:
     * <ul>
     *   <li>абсолютным URL (например, {@code "https://example.com/images/user123.jpg"});</li>
     *   <li>относительным путём (например, {@code "/images/user123.jpg"}).</li>
     * </ul>
     */
    private String authorImage;

    /**
     * Имя автора комментария (первое имя/имя для отображения).
     *
     * <p>Используется для показа имени пользователя рядом с комментарием.
     */
    private String authorFirstName;

    /**
     * Временная метка создания комментария в формате UNIX‑времени (миллисекунды с 1 января 1970 года).
     *
     * <p>Позволяет:
     * <ul>
     *   <li>сортировать комментарии по времени создания;</li>
     *   <li>вычислять возраст комментария;</li>
     *   <li>форматировать дату/время для отображения в интерфейсе.</li>
     * </ul>
     */
    private long createdAt;

    /**
     * Первичный ключ (уникальный идентификатор) комментария в системе.
     *
     * <p>Используется для:
     * <ul>
     *   <li>идентификации конкретного комментария при редактировании/удалении;</li>
     *   <li>ссылки на комментарий в API‑запросах;</li>
     *   <li>связывания с другими сущностями (например, с объявлением или постом).</li>
     * </ul>
     */
    private int pk;

    /**
     * Текстовое содержание комментария.
     *
     * <p>Основной текст сообщения, оставленного пользователем. Может содержать:
     * <ul>
     *   <li>обычный текст;</li>
     *   <li>эмодзи;</li>
     *   <li>короткие ссылки (если разрешено правилами системы).</li>
     * </ul>
     *
     * <p><b>Примечание:</b> максимальная длина и допустимые символы определяются бизнес‑логикой приложения.
     */
    private String text;
}
