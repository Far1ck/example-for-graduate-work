package ru.skypro.homework.dto;

import lombok.Data;

/**
 * Data Transfer Object (DTO) для передачи данных аутентификации пользователя.
 *
 * <p>Класс представляет собой простую структуру данных, содержащую информацию,
 * необходимую для процесса входа в систему (логина): имя пользователя и пароль.
 * Используется для передачи учётных данных от клиента к серверу при аутентификации.
 *
 * <p>Основные поля:
 * <ul>
 *   <li>{@code username} — идентификатор пользователя (например, email или логин);</li>
 *   <li>{@code password} — пароль пользователя в исходном виде (до кодирования).</li>
 * </ul>
 *
 * <p><b>Примечание:</b> поскольку класс помечен аннотацией {@link Data} от Lombok,
 * он автоматически получает:
 * <ul>
 *   <li>геттеры для всех полей;</li>
 *   <li>сеттеры для всех полей;</li>
 *   <li>реализацию {@code equals()} и {@code hashCode()};</li>
 *   <li>реализацию {@code toString()};</li>
 *   <li>конструктор по умолчанию.</li>
 * </ul>
 *
 * @see Data
 */
@Data
public class Login {

    /**
     * Имя пользователя (логин), используемое для аутентификации.
     *
     * <p>Может представлять собой:
     * <ul>
     *   <li>email-адрес;</li>
     *   <li>уникальный логин, назначенный при регистрации.</li>
     * </ul>
     */
    private String username;

    /**
     * Пароль пользователя в исходном (незашифрованном) виде.
     *
     * <p><b>Важно:</b> пароль передаётся в открытом виде только на этапе DTO,
     * перед обработкой должен быть надёжно зашифрован (например, с помощью BCrypt)
     * и никогда не должен храниться или логироваться в исходном виде.
     */
    private String password;
}
