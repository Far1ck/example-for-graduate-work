package ru.skypro.homework.dto;

import lombok.Data;

/**
 * Data Transfer Object (DTO) для представления информации о пользователе.
 *
 * <p>Используется для передачи данных между слоями приложения (например, между сервисом и контроллером)
 * при операциях получения/обновления информации о пользователях. Содержит основные публичные
 * атрибуты пользователя без чувствительной информации (например, пароля).
 *
 * <p>Поля DTO соответствуют полям сущности {@link ru.skypro.homework.entity.UserEntity},
 * но представлены в упрощённом виде для внешнего использования.
 *
 * <p><b>Примечание:</b> класс аннотирован {@link Data} от Lombok, что автоматически генерирует:
 * <ul>
 *   <li>геттеры и сеттеры для всех полей;</li>
 *   <li>реализацию {@code equals()} и {@code hashCode()};</li>
 *   <li>реализацию {@code toString()};</li>
 *   <li>конструктор по умолчанию.</li>
 * </ul>
 *
 * @see Data
 * @see ru.skypro.homework.entity.UserEntity
 * @see Role
 */
@Data
public class User {

    /**
     * Уникальный идентификатор пользователя в системе.
     *
     * <p>Соответствует полю {@code id} в сущности {@link ru.skypro.homework.entity.UserEntity}.
     * Используется для однозначной идентификации пользователя при операциях чтения/обновления.
     */
    private int id;

    /**
     * Электронный адрес пользователя, используемый как логин для аутентификации.
     *
     * <p>Должен быть уникальным в пределах системы. Отображается в интерфейсе
     * как основной идентификатор пользователя.
     */
    private String email;

    /**
     * Имя пользователя.
     *
     * <p>Отображается в профиле пользователя и в списках (например, в комментариях).
     */
    private String firstName;

    /**
     * Фамилия пользователя.
     *
     * <p>Отображается в профиле пользователя и в списках вместе с именем.
     */
    private String lastName;

    /**
     * Контактный телефон пользователя.
     */
    private String phone;

    /**
     * Роль пользователя в системе, определяющая его права доступа.
     *
     * <p>Принимает значения из перечисления {@link Role} (например, {@code USER}, {@code ADMIN}).
     * Используется для контроля доступа к функциональным возможностям системы.
     *
     * @see Role
     */
    private Role role;

    /**
     * Путь к аватару пользователя (относительный или абсолютный URL).
     *
     * <p>Может быть пустым, если пользователь не загрузил изображение. Используется
     * для отображения профиля в интерфейсе (например, рядом с комментариями или объявлениями).
     */
    private String image;
}
