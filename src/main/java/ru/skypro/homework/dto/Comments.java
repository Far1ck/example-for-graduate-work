package ru.skypro.homework.dto;

import lombok.Data;

import java.util.List;

/**
 * DTO (Data Transfer Object) для представления списка комментариев с пагинацией.
 *
 * <p>Используется для передачи структурированных данных о наборе комментариев, включая:
 * <ul>
 *   <li>общее количество комментариев в выборке ({@code count});</li>
 *   <li>список самих комментариев ({@code results}).</li>
 * </ul>
 *
 * <p>Типичные сценарии использования:
 * <ul>
 *   <li>ответ API при запросе списка комментариев (например, к объявлению или посту);</li>
 *   <li>передача данных между слоями приложения;</li>
 *   <li>сериализация в JSON для клиентских приложений.</li>
 * </ul>
 *
 * <p>Класс аннотирован {@link Data}, что автоматически генерирует:
 * <ul>
 *   <li>геттеры и сеттеры для всех полей;</li>
 *   <li>методы {@code equals()} и {@code hashCode()};</li>
 *   <li>метод {@code toString()};</li>
 *   <li>конструктор без аргументов.</li>
 * </ul>
 *
 * @see Data
 * @see Comment
 */
@Data
public class Comments {

    /**
     * Общее количество комментариев в текущей выборке/фильтре.
     *
     * <p>Используется для:
     * <ul>
     *   <li>отображения общего числа комментариев пользователю (например, «15 комментариев»);</li>
     *   <li>реализации пагинации (расчёт количества страниц);</li>
     *   <li>индикации наличия комментариев (если {@code count == 0}, комментариев нет).</li>
     * </ul>
     *
     * <p><b>Примечание:</b> значение может отражать:
     * <ul>
     *   <li>общее число комментариев в системе;</li>
     *   <li>число комментариев, соответствующих определённому фильтру (например, к конкретному объекту);</li>
     *   <li>количество комментариев на текущей странице при пагинации.</li>
     * </ul>
     */
    private int count;

    /**
     * Список комментариев, входящих в текущую выборку.
     *
     * <p>Каждый элемент — объект класса {@link Comment}, содержащий:
     * <ul>
     *   <li>данные об авторе (ID, имя, изображение);</li>
     *   <li>время создания;</li>
     *   <li>текст комментария;</li>
     *   <li>уникальный идентификатор комментария.</li>
     * </ul>
     *
     * <p>Особенности:
     * <ul>
     *   <li>может быть пустым ({@code null} или {@code empty list}), если комментариев нет;</li>
     *   <li>порядок элементов обычно соответствует логике сортировки (например, по времени создания);</li>
     *   <li>размер списка может ограничиваться пагинацией (например, 10 комментариев на страницу).</li>
     * </ul>
     *
     * @see Comment
     */
    private List<Comment> results;
}
