package ru.skypro.homework.dto;

import jakarta.validation.constraints.Max;
import jakarta.validation.constraints.Min;
import jakarta.validation.constraints.Size;
import lombok.Data;

/**
 * Data Transfer Object (DTO) для создания и обновления объявлений в системе.
 *
 * <p>Используется в API для приёма и валидации входных данных при:
 * <ul>
 *   <li>создании нового объявления;</li>
 *   <li>редактировании существующего объявления.</li>
 * </ul>
 *
 * <p>Содержит только те поля, которые пользователь может редактировать, с наложенными
 * ограничениями валидации через аннотации Jakarta Validation.
 *
 * <p><b>Примечание:</b> класс аннотирован {@link Data} от Lombok, что автоматически генерирует:
 * <ul>
 *   <li>геттеры и сеттеры для всех полей;</li>
 *   <li>реализацию {@code equals()} и {@code hashCode()};</li>
 *   <li>реализацию {@code toString()};</li>
 *   <li>конструктор по умолчанию.</li>
 * </ul>
 *
 * @see Data
 * @see jakarta.validation.constraints.Size
 * @see jakarta.validation.constraints.Min
 * @see jakarta.validation.constraints.Max
 */
@Data
public class CreateOrUpdateAd {

    /**
     * Заголовок объявления, кратко описывающий товар или услугу.
     *
     * <p>Применяются ограничения валидации:
     * <ul>
     *   <li>{@code min = 4} — минимальная длина 4 символа;</li>
     *   <li>{@code max = 32} — максимальная длина 32 символа.</li>
     * </ul>
     *
     * <p>При несоответствии ограничениям будет выброшено исключение валидации
     * с сообщением о нарушении правил ввода.
     *
     * @see Size
     */
    @Size(min = 4, max = 32)
    private String title;

    /**
     * Стоимость товара или услуги, указанная в объявлении.
     *
     * <p>Применяются ограничения валидации:
     * <ul>
     *   <li>{@code value = 0} — минимальное значение 0 (бесплатно);</li>
     *   <li>{@code value = 10000000} — максимальное значение 1 000 000 условных единиц.</li>
     * </ul>
     *
     * <p>Значение указывается в условных единицах (например, рублях). При выходе
     * за допустимый диапазон будет выброшено исключение валидации.
     *
     * @see Min
     * @see Max
     */
    @Min(value = 0)
    @Max(value = 10000000)
    private int price;

    /**
     * Подробное описание товара или услуги, предлагаемой в объявлении.
     *
     * <p>Применяются ограничения валидации:
     * <ul>
     *   <li>{@code min = 8} — минимальная длина 8 символов;</li>
     *   <li>{@code max = 64} — максимальная длина 64 символа.</li>
     * </ul>
     *
     * <p>Может содержать ключевые характеристики, условия сделки, особенности
     * товара и т. п. При несоответствии ограничениям будет выброшено
     * исключение валидации.
     *
     * @see Size
     */
    @Size(min = 8, max = 64)
    private String description;
}
