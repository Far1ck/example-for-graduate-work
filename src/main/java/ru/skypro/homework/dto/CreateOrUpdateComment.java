package ru.skypro.homework.dto;

import jakarta.validation.constraints.Size;
import lombok.Data;

/**
 * DTO (Data Transfer Object) для создания или обновления комментария в системе.
 *
 * <p>Представляет собой упрощённую структуру данных, содержащую только те поля,
 * которые необходимы для операции создания или редактирования комментария. Используется:
 * <ul>
 *   <li>в API‑контроллерах для приёма входных данных от клиента;</li>
 *   <li>для валидации текста комментария перед сохранением;</li>
 *   <li>как контракт между фронтендом и бэкендом при отправке/обновлении комментариев.</li>
 * </ul>
 *
 * <p>Класс аннотирован {@link Data}, что автоматически генерирует:
 * <ul>
 *   <li>геттеры и сеттеры для всех полей;</li>
 *   <li>методы {@code equals()} и {@code hashCode()};</li>
 *   <li>метод {@code toString()};</li>
 *   <li>конструктор без аргументов.</li>
 * </ul>
 *
 * @see Data
 * @see Size
 */
@Data
public class CreateOrUpdateComment {

    /**
     * Текст комментария, который пользователь хочет создать или обновить.
     *
     * <p>К полю применена валидация через аннотацию {@link Size}:
     * <ul>
     *   <li>{@code min = 8} — минимальная длина текста составляет 8 символов;</li>
     *   <li>{@code max = 64} — максимальная длина текста ограничена 64 символами.</li>
     * </ul>
     *
     * <p>Если значение не соответствует указанным ограничениям, будет выброшено исключение
     * валидации (например, при обработке запроса в контроллере).
     *
     * <p><b>Примечание:</b> ограничения на длину могут быть скорректированы в соответствии
     * с бизнес‑требованиями системы.
     *
     * @see Size
     */
    @Size(min = 8, max = 64)
    private String text;
}
