package ru.skypro.homework.dto;

import lombok.Data;

/**
 * Data Transfer Object (DTO) для передачи данных регистрации нового пользователя.
 *
 * <p>Класс представляет собой структуру данных, содержащую всю необходимую информацию
 * для создания учётной записи пользователя в системе. Используется при обработке
 * запросов на регистрацию новых пользователей.
 *
 * <p>Основные поля:
 * <ul>
 *   <li>{@code username} — уникальный идентификатор пользователя (логин);</li>
 *   <li>{@code password} — пароль пользователя в исходном виде;</li>
 *   <li>{@code firstName} — имя пользователя;</li>
 *   <li>{@code lastName} — фамилия пользователя;</li>
 *   <li>{@code phone} — контактный телефон пользователя;</li>
 *   <li>{@code role} — роль пользователя в системе (определяет права доступа).</li>
 * </ul>
 *
 * <p><b>Примечание:</b> класс аннотирован {@link Data} от Lombok, что автоматически генерирует:
 * <ul>
 *   <li>геттеры и сеттеры для всех полей;</li>
 *   <li>реализацию методов {@code equals()} и {@code hashCode()};</li>
 *   <li>реализацию метода {@code toString()};</li>
 *   <li>конструктор по умолчанию.</li>
 * </ul>
 *
 * @see Data
 * @see Role
 */
@Data
public class Register {

    /**
     * Уникальный идентификатор пользователя (логин), используемый для аутентификации.
     *
     * <p>Обычно представляет собой:
     * <ul>
     *   <li>email-адрес;</li>
     *   <li>или иной уникальный идентификатор, задаваемый при регистрации.</li>
     * </ul>
     *
     * <p><b>Требования:</b>
     * <ul>
     *   <li>обязательное поле;</li>
     *   <li>должно быть уникальным в системе.</li>
     * </ul>
     */
    private String username;

    /**
     * Пароль пользователя в исходном (незашифрованном) виде.
     *
     * <p><b>Важно:</b>
     * <ul>
     *   <li>пароль передаётся в открытом виде только на уровне DTO;</li>
     *   <li>перед сохранением в хранилище должен быть надёжно зашифрован (например, BCrypt);</li>
     *   <li>запрещено хранить или логировать в исходном виде.</li>
     * </ul>
     *
     * <p><b>Требования:</b> должно соответствовать политике безопасности паролей системы.
     */
    private String password;

    /**
     * Имя пользователя.
     *
     * <p><b>Требования:</b>
     * <ul>
     *   <li>обязательное поле;</li>
     *   <li>длина и формат определяются бизнес‑правилами системы.</li>
     * </ul>
     */
    private String firstName;

    /**
     * Фамилия пользователя.
     *
     * <p><b>Требования:</b>
     * <ul>
     *   <li>обязательное поле;</li>
     *   <li>длина и формат определяются бизнес‑правилами системы.</li>
     * </ul>
     */
    private String lastName;

    /**
     * Контактный телефон пользователя.
     *
     * <p><b>Требования:</b>
     * <ul>
     *   <li>может быть необязательным полем;</li>
     *   <li>формат должен соответствовать принятым в системе стандартам (например, E.164).</li>
     * </ul>
     */
    private String phone;

    /**
     * Роль пользователя в системе, определяющая его права доступа и возможности.
     *
     * <p>Значение задаётся на основе:
     * <ul>
     *   <li>выбора пользователя при регистрации (если доступно);</li>
     *   <li>или назначения администратором системы.</li>
     * </ul>
     *
     * <p><b>Примечание:</b> тип {@link Role} должен определять перечень допустимых ролей
     * и их семантику в контексте приложения.
     *
     * @see Role
     */
    private Role role;
}
